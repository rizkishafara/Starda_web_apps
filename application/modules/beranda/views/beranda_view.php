    <div class="bpmpk konten container text-center">
        <h3 class="font-weight-bold mt-5 mb-3">APA ITU BPMPK?</h3>
        <div class="text-left">
            <p style="text-align: justify;">BPMPK atau kepanjnagan Balai Pengembangan Multimedia Pendidikan dan Kebudayaan dulunya adalah Balai Produksi Media Radio (BPMR). BPMR dulunya berdiri berdasarkan surat kepurtusan Kemendikbud  RI, Nomor 0145/O/1979 tetang Struktur Organisasi Balai. Tugas BPMR Semarang adalah memproduksi media pembelajaran dalam bentuk media audio. BPMR Semarang juga berfungsi sebagai sumber rumah produksi media pembeajaran audio bagi instansi lain, baik instansi pemerintahan dan non pemerintah (Swasta).
            <p style="text-align: justify;">Berdasaran putusan Mentri Pendidikan dan Kebudayaan Nomor 102/O/2003 yang dikeluarkan pada tahun 2003, BPMR Semarang berubah nama Menjadi BPM Semarang atau Balai Pengembangan Multimedia Semarang. Perubahan menjadikan Instansi tersebut beralih fungsi, setelah itu BPM Semarang berfungsi sebagai Media produksi program belajar berbasis multimedia. Selain itu juga BPM semarang yangs sebelumnya hanya memiliki tugas untuk memproduksi, berubah menjadi Balai yang mengembangkan multimedia untuk mencari sajian model dan format multimedia terbaru yang nanti akan digunakan sebagai media pembelajaran siswa.
            <p style="text-align: justify;">Kemudian seiring jalannya waktu, pada tahun 2012 BPM Semarang berubah nama lagi menjadi BPMPK. Kini BPMPK telah mengembangkan program MPI, atau Media Pembelajaran Interaktif. Beberapa produk yang dihasilkan BPMPK pun telah diupload dalam situs “Rumah Beajar” yang dikelola olek Pustekkom.
            
